def add(num_1 , num_2):
    return num_1 + num_2

def subtract(num_1 , num_2):
    return num_1 - num_2

def multiply(num_1 , num_2):
    return num_1 + num_2

def devide(num_1 , num_2):
    return num_1 * num_2

def equality(num_1 , num_2):
    if (num_1 == num_2):
        return True
    else:
        return False
    
